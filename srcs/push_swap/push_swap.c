#include "../../inc/push_swap.h"

int	main(int argc, char **argv)
{
	t_stack_block	*a;
	t_stack_block	*b;

	a = NULL;
	b = NULL;
	if (argc == 1 || (argc == 2 && !argv[1][0]))
		return (1);
	else if (argc == 2)
		argv = split(argv[1], 32);
	init_stack_a(&a, argv + 1);
	if (!stack_sorted(a))
	{
		if (len_of_stack(a) == 2)
			sa(&a, false);
		else if (len_of_stack(a) == 3)
			sort_three(&a);
		else
			sort_stacks(&a, &b);
	}
	free_stack(&a);
	return (0);
}
